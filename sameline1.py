#multiplication of two number in same line
a,b=[int(x) for x in input("Enter three number : ").split()]
print("The product of a and b :",a*b)

#multiplication of three number in same line
a,b,c=[int(x) for x in input("Enter three number : ").split()]
print("The product of a and b :",a*b*c)

#multiplication of four number in same line
a,b,c,d=[float(x) for x in input("Enter three number : ").split(',')]
print("The product of a and b :",a*b*c*d)